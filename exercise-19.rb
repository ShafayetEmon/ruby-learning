def sum(arg1, arg2)
    puts "Summation = #{arg1 + arg2}"
end

def sub(arg1, arg2)
    puts "Subtraction = #{arg1 - arg2}"
end

def mul(arg1, arg2)
    puts "Multiplication = #{arg1 * arg2}"
end

def div(arg1, arg2)
    puts "Division = #{arg1 / arg2}"
end

sum(10, 10)
sub(10, 10)
mul(10, 10)
div(10, 10)